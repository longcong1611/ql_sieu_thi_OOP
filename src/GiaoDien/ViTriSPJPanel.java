    public void View(){
        x = listVT.get(pos);
        this.txtMaViTri.setText(""+x.getMaViTri());
        this.txtMaSP.setText(""+x.getMaSP());
        this.txtKhuVuc.setText(x.getKhuVuc());
        this.txtKeSo.setText(""+x.getKeSo());
        this.txtTang.setText(""+x.getTang());
        this.txtMoTa.setText(x.getMoTa());
        this.txtSoLuong.setText(""+x.getSoLuongHienCo());
        this.txtNgayCapNhat.setText(""+formatter.format(x.getNgayCapNhat()));
        OnOff(true, false);
    }
    
    public void ViewTable(String ma)
    {
        DefaultTableModel model = (DefaultTableModel) this.tblViTri.getModel();
        model.setNumRows(0);
        for (ViTriSP x:listVT)
        {
            if ((""+x.getMaSP()).contains(ma))
            {
                 model.addRow(new Object[]{x.getMaViTri(),x.getMaSP(),x.getKhuVuc(), x.getKeSo(), x.getTang(), x.getMoTa(), x.getSoLuongHienCo(), formatter.format(x.getNgayCapNhat())});
            }
        }
    }
    
    public ViTriSP Search(int s)
    {
        for (ViTriSP x:listVT)
        {
            if (x.getMaViTri() == s)
                return x;
        }
        return x;
    }
    
