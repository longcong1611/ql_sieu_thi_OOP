    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        listVT.remove(pos);
            if (pos > listVT.size()-1) 
                pos = pos -1;
            if (pos <0)
                pos = 0;
        int MaViTri = Integer.parseInt(this.txtMaViTri.getText());
        vtsql.xoaViTri(MaViTri);    
        View();
        ViewTable(this.txtTimMaSP.getText());
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        int MaViTri = Integer.parseInt(this.txtMaViTri.getText());
        int MaSP = Integer.parseInt(this.txtMaSP.getText());
        String KhuVuc = this.txtKhuVuc.getText();
        int KeSo = Integer.parseInt(this.txtKeSo.getText());
        int Tang = Integer.parseInt(this.txtTang.getText());
        String MoTa = this.txtMoTa.getText();
        int SoLuongHienCo = Integer.parseInt(this.txtSoLuong.getText());
        Date NgayCapNhat = null;
        try {
            NgayCapNhat = formatter.parse(this.txtNgayCapNhat.getText());
        } catch (ParseException ex) {
            Logger.getLogger(ViTriSPJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (check == 1){
            ViTriSP vtthem = new ViTriSP(MaViTri, MaSP, KhuVuc, KeSo, Tang, MoTa, SoLuongHienCo, NgayCapNhat);
            this.listVT.add(vtthem);
            vtsql.themViTri(vtthem);
        }
        else{
            ViTriSP vtsua = new ViTriSP(MaViTri, MaSP, KhuVuc, KeSo, Tang, MoTa, SoLuongHienCo, NgayCapNhat);
            this.listVT.set(pos, vtsua);
            vtsql.suaViTri(vtsua);
        }
        View();
        ViewTable(this.txtTimMaSP.getText());
    }//GEN-LAST:event_btnSaveActionPerformed

