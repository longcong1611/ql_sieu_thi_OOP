    private void thongKeTheoNgay() {
    java.util.Date tuNgay = jDateChooser2.getDate();
    java.util.Date denNgay   = jDateChooser1.getDate();
    if (tuNgay == null || denNgay == null) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn đầy đủ từ ngày và đến ngày");
        return;
    }
    // Gọi DAL
    List<ThongKe> danhSach = new ThongKeSQL().getThongKeTheoNgay(tuNgay, denNgay);
    if (danhSach == null) {
        danhSach = new ArrayList<>();
    }
    // Đổ dữ liệu vào bảng
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);
    int tong = 0;
    for (ThongKe tk : danhSach) {
        model.addRow(new Object[] {
            tk.getNgayLap(),
            tk.getMaHD(),
            tk.getTongTien(),
            tk.getMaNV(),
            tk.getNhanVienLap()
        });
        tong += tk.getTongTien();
    }
    // Hiển thị tổng tiền
    jTextField1.setText(new DecimalFormat("#,###").format(tong));
}
