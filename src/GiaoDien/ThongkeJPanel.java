    private void thongKeTheoTen() {
    // Lấy tên nhân viên từ combobox
    String tenNV = (String) jComboBox1.getSelectedItem();

    if (tenNV == null || tenNV.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn tên nhân viên");
        return;
    }

    // Gọi DAL
    ThongKeSQL tkDAL = new ThongKeSQL();
    List<ThongKe> danhsach = tkDAL.getThongKeTheoTen(tenNV);
    if(danhsach == null){
        danhsach = new ArrayList<>();
    }
    // Hiển thị dữ liệu ra bảng
    DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
    model.setRowCount(0); // Xóa dòng cũ

    int tongDoanhThu = 0;

    for (ThongKe tk : danhsach) {
        model.addRow(new Object[]{
            tk.getNgayLap(),
            tk.getMaHD(),
            tk.getMaNV(),
            tk.getTongTien()
        });
        tongDoanhThu += tk.getTongTien();
    }

    // Hiển thị tổng tiền vào text field
    DecimalFormat formatter = new DecimalFormat("#,###");
    jTextField2.setText(formatter.format(tongDoanhThu));
}
    private void thongKeSanPhamTheoLoai() {
    String tenLoai = (String) jComboBox2.getSelectedItem();
    java.util.Date tuNgay = jDateChooser4.getDate();
    java.util.Date denNgay = jDateChooser5.getDate();

    if (tenLoai == null || tuNgay == null || denNgay == null) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn loại sản phẩm và khoảng thời gian");
        return;
    }

    ThongKeSQL tkDAL = new ThongKeSQL();
    List<ThongKe> list = tkDAL.getThongKeSanPham(tuNgay, denNgay, tenLoai);

    DefaultTableModel model = (DefaultTableModel) jTable3.getModel(); // bảng bên tab sản phẩm
    model.setRowCount(0);

    int tongBanRa = 0;
    int tongConLai = 0;

    for (ThongKe sp : list) {
        model.addRow(new Object[]{
            sp.getMaSP(),
            sp.getTenSP(),
            sp.getTongNhap(),
            sp.getSoluongBanra(),
            sp.getSoluongConlai(),
        });

        tongBanRa += sp.getSoluongBanra();
        tongConLai += sp.getSoluongConlai();
    }

    DecimalFormat formatter = new DecimalFormat("#,###");
    jTextField3.setText(formatter.format(tongBanRa));
    jTextField4.setText(formatter.format(tongConLai));
}

