    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        listKH.remove(pos);
            if (pos > listKH.size()-1) 
                pos = pos -1;
            if (pos <0)
                pos = 0;
        int MaKH = Integer.parseInt(this.txtMaKH.getText());
        khsql.xoaKhachHang(MaKH);
        View();
        ViewTable(this.txtTimTenKH.getText());
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        int MaKH = Integer.parseInt(this.txtMaKH.getText());
        String TenKH = this.txtTenKH.getText();
        String SDT = this.txtSDT.getText();
        String GioiTinh = this.txtGioiTinh.getText();
        if (check == 1){
            KhachHang khthem = new KhachHang(MaKH,TenKH,SDT,GioiTinh);
            this.listKH.add(khthem);
            khsql.themKhachHang(khthem);
        }
        else{
            KhachHang khsua = new KhachHang(MaKH,TenKH,SDT,GioiTinh);
            this.listKH.set(pos, khsua);
            khsql.suaKhachHang(khsua);
        }
        View();
        ViewTable(this.txtTimTenKH.getText());
    }//GEN-LAST:event_btnSaveActionPerformed

