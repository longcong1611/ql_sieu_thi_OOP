    public void View()
    {
        x = listKH.get(pos);
        this.txtMaKH.setText(""+x.getMaKH());
        this.txtTenKH.setText(x.getTenKH());
        this.txtSDT.setText(x.getSDT());
        this.txtGioiTinh.setText(x.getGioiTinh());
        OnOff(true, false);
    }
    
        public void OnOff(boolean a, boolean b)
    {
        this.btnSave.show(b);
        this.btnCancel.show(b);

        this.btnAdd.show(a);
        this.btnEdit.show(a);
        this.btnDelete.show(a);
    }
    
    public void ViewTable(String name)
    {
        //Show table
        DefaultTableModel model = (DefaultTableModel) this.tblKhachHang.getModel();
        model.setNumRows(0);
        for (KhachHang x:listKH)
        {
            if (x.getTenKH().contains(name))
            {
                 model.addRow(new Object[]{x.getMaKH(),x.getTenKH(),x.getSDT(),x.getGioiTinh()});
            }
        }
    }
    
    public KhachHang Search(int s)
    {
        for (KhachHang x:listKH)
        {
            if (x.getMaKH()== s)
                return x;
        }
        return x;
    }

